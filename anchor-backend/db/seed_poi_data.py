TRAN_MODE_FACTOR = {"汽车": 1.0, "地铁": 1.2, "骑行": 1.6, "步行": 2.0}


def seed_poi_tables(conn: sqlite3.Connection) -> dict[str, int]:
    """写入 POI 与交通段，返回各表插入条数。"""
    counts: dict[str, int] = {}

    conn.executemany(
        """
        INSERT OR REPLACE INTO attractions (
            id, name, city, address, longitude, latitude, rating,
            attract_tier, attract_time, attract_factor, open_time, ticket_price,
            visit_hours, brief_intro
        ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
        """,
        [
            (
                r[0], r[1], r[2], r[3], r[4], r[5], r[6], r[7], r[8],
                TIER_FACTOR[r[7]], r[9], r[10], r[8],
                ATTRACTION_BRIEF_INTROS.get(r[0], ""),
            )
            for r in ATTRACTIONS
        ],
    )
    counts["attractions"] = len(ATTRACTIONS)

    conn.executemany(
        """
        INSERT OR REPLACE INTO hotels (
            id, name, city, address, longitude, latitude, rating, star_level, price_range
        ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)
        """,
        HOTELS,
    )
    counts["hotels"] = len(HOTELS)

    conn.executemany(
        """
        INSERT OR REPLACE INTO restaurants (
            id, name, city, address, longitude, latitude, rating, cuisine_type, price_range
        ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)
        """,
        RESTAURANTS,
    )
    counts["restaurants"] = len(RESTAURANTS)

    conn.executemany(
        """
        INSERT OR REPLACE INTO traffic_segment (
            from_poi_id, from_poi_type, to_poi_id, to_poi_type,
            tran_time, tran_mode, tran_factor, city
        ) VALUES (?, ?, ?, ?, ?, ?, ?, ?)
        """,
        [
            (*row[:6], TRAN_MODE_FACTOR.get(row[5], 1.0), row[6])
            for row in TRAFFIC
        ],
    )
    counts["traffic_segment"] = len(TRAFFIC)

    return counts


